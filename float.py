from decimal import Rounded


num1 = 5.0
num2 = 2.5

#addition
result1 = num1 + num2
print("addition:", result1)

#substraction
result2 = num1 - num2
print("substraction:", result2)

#multiplication
result3 = num1 * num2
print("multiplication:" ,result3)

#division
result4 = num1 / num2
print("Division:", result4)

#RoundOff to 2 digits
result5 = round(3.36432463246324823482, 2)
